def multiply(a, b):
    """
    Calculate the multiplication of two numbers.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        The product of a and b
    """
    return a * b


if __name__ == "__main__":
    # Example usage
    result = multiply(5, 3)
    print(f"5 × 3 = {result}")
    
    result = multiply(10, 7)
    print(f"10 × 7 = {result}")
    

